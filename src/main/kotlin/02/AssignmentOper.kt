package `02`

fun main(args: Array<String>) {
    var num1: Int = 10
    var num2: Int = 3

    num1 += num2 // 10 + 3 = 13
    println(num1)
    num1 -= num2 // 13 - 3 = 10
    println(num1)
    num1 *= num2 // 10 * 3 = 30
    println(num1)
    num1 /= num2 // 30 / 3 = 10
    println(num1)
    num1 %= num2 // 10 % 3 = 1
    println(num1)
}